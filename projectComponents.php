            <br/>
            <h3 style='color: #3289C7;'>Mangrove</h3>
             <span style='font-size: 1.3em;'><strong>Mapping and Assessing Mangrove Forests</strong></span><br/>
          </div>
        </div>
        <div class='row'>
          <div class="col-lg-12 col-md-12" style="text-align: justify;">
            <br/>
              <p style="font-size: 1.4em;">
            Mangroves are trees, shrubs, or palms that are adapted to grow in the intertidal zone above mean sea level. These plants have evolved to survive such harsh environments by developing features such as high tolerance for salt, specialized prop roots and breathing roots, and waxy leaves that keep them from drying out. Mangroves offer a variety of ecosystem services, including habitat, spawning and nursery area of marine and even terrestrial organisms. They also sequester atmospheric carbon, export nutrients and organic materials, improve water quality by filtering sediments, and protect and stabilize coastlines.
            </p>
             <p style="font-size: 1.4em;">
               Mangrove forest of the Philippines have been continuously declining in the last century, mainly because mangroves areas are being converted to shrimp and fish ponds, while the wood are used as building materials and firewood. There is also very few research and conservation efforts, and lack of public awareness about the importance of mangroves.
             </p>
               <p style="font-size: 1.4em;">With this, we aim to assess the status of mangroves in our NIPAS sites, and evaluate mangrove community structure by conducting field surveys. Prior to field work, NAMRIA topographic maps (1950s) and recent satellite images were used to know historical and present extents of mangroves in these areas, and to select sites for ground truth. Along with these, we also held focus group discussions to know the perception of locals about habitat changes and issues. We also calculated vulnerability to erosion, inundation, and other causes of habitat change of the area is calculated.</p>
