            <h3 style='color: #3289C7;'>REEF FISH SURVEY</h3>
          </div>
        </div>
        <div>
         <span style='font-size: 1.3em;'><strong>Monitoring coral reef fish assemblages</strong></span><br/>
        </div>
        <div class='row'>
          <div class="col-lg-12 col-md-12" style="text-align: justify;">
            <br/>
            <p style='font-size: 1.4em'>
              Reef fish are some of the most highly prized, economically important species, and many Filipinos rely heavily on fisheries products for both food and livelihood. Fish populations have been steadily declining due to overexploitation and destructive human activities, and in order to understand the status and trends in reef fish populations, we need to assess and monitor key characteristics of fish assemblages in our NIPAS sites.
            </p>
            <p style='font-size: 1.4em'>
             Reef fish community assessment was conducted using a modified fish visual census (English et al., 1997). Community parameters collected were species richness, abundance, and biomass of reef fish assemblage in each station. Fishes within three 50 x 10 meter transect were identified, counted and estimated for their sizes. With this, we aim to assess the reef fish community structure and determine the current status of the NIPAS areas. 
            </p>
            <p style='font-size: 1.4em'>
           DENR-BMB and Regional Offices were trained on conducting fish visual census, methods on assessment and monitoring, and data handling and analyses, which they use on our NIPAS sites.	
            </p><br/>
