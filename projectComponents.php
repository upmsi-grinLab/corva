            <h3 style='color: #3289C7;'>CONNECTIVITY</h3>
            <strong style='font-size: 1.3em;'>Ocean Modeling for Coral Reef Connectivity</strong>
          </div>
        </div>
        <div class='row'>
          <div class="col-lg-12 col-md-12" style="text-align: justify;">
            <br/>
            <p style='font-size: 1.4em;'>
            Coral reefs, seagrass beds, mangroves, and other coastal habitats are patchily distributed throughout our seas; but all these habitats are interconnected because ocean currents carry and move nutrients, eggs, larvae, even organisms and pollutants among and between these reefs and other environments. 
            </p>
            <p style='font-size: 1.4em;'>
              Connectivity, or the flux of particles between and within protected areas is important because it helps maintain diversity and ecological resilience. With this, we aim to determine the connectivity of all the coral reefs of the Philippines using the HYbrid Coordinate Ocean Model (HYCOM; hycom.org) to simulate dispersal patterns for coral larvae. HYCOM is an ocean circulation model that optimally simulates coastal and open-ocean circulation features. Information from these models are important considerations in establishing MPAs and MPA networks in the area. 
            </p>
