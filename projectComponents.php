            <br/>
            <h3 style='color: #3289C7;'>Deep Coral Reefs</h3>
          </div>
        </div>
          <div>
         <span style='font-size: 1.3em;'><strong>Exploring Mesophotic Coral Reef Ecosystems</strong></span><br/>
        </div>
        <div class='row'>
          <div class="col-lg-12 col-md-12" style="text-align: justify;">
            <br/>
            <p style='font-size: 1.4em;'>Mesophotic ecosystems are extensions of shallow coral reefs that are located 30m-200m deep waters, where sunlight barely penetrates. Like shallow coral reefs, mesophotic reefs also harbour numerous coral species that are adapted to maximize light capture. Fish and other marine organisms also tend to be bigger and higher in number. </p>
           <p style='font-size: 1.4em;'>
             Deep coral reefs are potential refugia of marine organisms, as they are protected from fishing and other human activities. Mesophotic reefs are also foraging and spawning grounds, which makes them potential sources of larvae that can help rehabilitate degraded reefs.
            </p>
            <p style='font-size: 1.4em;'>
             With this, we aim to document the occurrence of corals in the mesophotic window (30-40m) in selected NIPAS sites, and also explore potential connectivity between the shallow reef and mesophotic depths.
            </p>
             <p style='font-size: 1.4em;'>
           We used remotely operated vehicles (ROVs) to capture images and video clips of corals, fish communities, and other benthic organisms. Bathymetric surveys were conducted using integrated GPS and single beam echosounders. Manual coral survey were also done in chosen dive sites that are within 30m-40 m water depths and at or close to a scarp. Corals that were encountered  were identified to genera level, while also identifying the associations of coral type with sea floor type and morphology.</p>
