            <h3 style='color: #3289C7;'>Seagrass</h3>
             <span style='font-size: 1.3em;'><strong>Mapping and Assessing Seagrass Meadows</strong></span><br/>
          </div>
        </div>
        <div class='row'>
          <div class="col-lg-12 col-md-12" style="text-align: justify;">
            <br/>
            <p style='font-size: 1.4em;'>Seagrasses are marine plants that, like terrestrial ones, have roots and flowers.Usually found in coastal areas with sandy to muddy bottoms, they need enough sunlight and a small amount of nutrients to  thrive. Seagrass beds provide food and shelter to and serve as breeding/nursery areas for numerous organisms, including shellfish and finfish. They also help protect coastlines from erosion and maintain water quality by acting as filters in areas where nutrient levels are too high.</p>
            <p style='font-size: 1.4em;'>Despite its importance, seagrasses are said to be the least studied the tropical coastal ecosystems. Seagrass communities are continuously threatened by decline in coastal water quality, reclamation and unregulated coastal development, degradation of environment and resources, and other human-induced disturbances.</p>
            <p style='font-size: 1.4em;'>With this, we aim to assess the status of seagrasses in our NIPAS sites, and evaluate seagrass growth and productivity by conducting field surveys. Prior to field work, NAMRIA topographic maps (1950s) and recent satellite images were used to know historical and present extents of seagrasses in these areas, and to select sites for ground truth. Along with these, we also held focus group discussions to know the perception of locals about habitat changes and issues. We also calculated vulnerability to erosion, inundation, and other causes of habitat change of the area.</p>
