          <h3 style='color: #3289C7;'>ARRAS</h3>
          <span style='font-size: 1.3em;'><strong>Automated Rapid Reef Assessment System</strong></span><br/>
        </div>
      </div>
      <div class='row'>
        <div class="col-lg-12 col-md-12" style="text-align: justify;">
          <br/>
          <p style='font-size: 1.4em;'>
         Effective management and sustainable use of our reefs require up-to-date information and regular monitoring of their current conditions. With this in mind, the ARRAS technology was developed with the goal of recording and monitoring of the Philippines’ vast coral reefs. 
		 </p>
          <p style='font-size: 1.4em;'>
         The Automated Rapid Reef Assessment System (ARRAS) is a fast, diver-less, low-cost technology developed by UP Diliman and Mapua Institute of Technology that enables large-scale monitoring of coral reefs. This technology creates permanent visual records of Philippine reefs, and covers wide areas in a short amount of time. 
          </p>
          <p style='font-size: 1.4em;'>
         ARRAS consists of towpedo, teardrop, underwater camera, GPS logger/echosounder, and a stitching software that enables users to cover a wide extent of underwater habitats and create permanent visuals records of these habitats. ARRAS captures underwater image of benthic habitats while being towed using a motorized boat. Images captured are then processed, and stitched images are embedded in Google Earth.
          </p>
          <p style='font-size: 1.4em'>
          DENR-BMB and Regional Offices were trained and equipped with the ARRAS technologies, which they use to monitor our NIPAS sites.
          </p>
