            <h3 style='color: #3289C7;'>COASTAL STABILITY</h3>
             <span style='font-size: 1.3em;'><strong>Coastal Integrity and Beach Profiling</strong></span><br/>
          </div>
        </div>
        <div class='row'>
          <div class="col-lg-12 col-md-12" style="text-align: justify;">
            <br/>
            <p style="font-size: 1.4em;">
            Coastal zone is the interface where the land meets the ocean, encompassing shoreline  environments as well as adjacent coastal waters. Being a maritime nation with 18,000 km of shoreline, many Filipinos are heavily reliant on coastal resources for sustenance and income. Hence it is important to know how the coast is changing, and if there are risks to life and infrastructure.
            </p>
            <p style="font-size: 1.4em;">
              With this, we aim to map and evaluate the current state of the shoreline and bathymetry near the shore, which can help us make initial inferences on the effect of current interventions being employed at these sites.
            </p>
            <p style="font-size: 1.4em;">
             The current state of the shore was evaluated using on site beach monitoring technique: walk-the-coast GPS survey that involves tracking the position of the shoreline by walking along the waterline and recording the position using a GPS receiver; and beach profiling by Emery Method for the measurement of the width and height of the beach and for tide correction. Historical shoreline and nearshore bathymetric changes were measured, and secondary data such as historical wind, typhoon, seismicity, rainfall, and tide were acquired to provide a better understanding on the erosion and accretion processes of the beach.
            </p>
            <br>
