            <br/>
            <h3 style='color: #3289C7;'>SHALLOW REEF</h3>
            <span style='font-size: 1.3em;'><strong>Smaller scale reef monitoring</strong></span><br/>
          </div>
        </div>
        <div class='row'>
          <div class="col-lg-12 col-md-12" style="text-align: justify;">
            <br/>
            <p style='font-size: 1.4em'>
            While ARRAS enables large-scale monitoring of our coral reefs, we also need accurate data at highly localized scales to precisely assess the impact of disturbance on reefs and track if our reefs are recovering or continuously declining.
			</p>
			 <p style='font-size: 1.4em'>
			The CoRVA Coral Survey uses a combined Reef Check and GEF-CRTR protocol. This is a simple and fast method that provides basic but essential data on coral reef health. The Reef Check protocol includes the use of a transect line to measure distance and area and a plumb-line to determine a point sample location along the line, and makes use of indicator organisms and substrates. The Global Environment Fund - Coral Reef Targeted Research (GEF-CRTR) protocol (van Woesik et al., 2009) was used as standard for sampling design and methods and includes the use of a tetrapod with a digital camera for photo-documentation.
			 </p>
			 <p style='font-size: 1.4em'>
			 With this, we aim to map the distribution of coral communities in our NIPAS sites, and assess the current state of the coral reefs using commonly used metrics such as hard coral cover and biodiversity, and their vulnerability and resilience to threats such as coral bleaching. We also established a monitoring system for some of our NIPAS sites to allow the quantification of changes in the structure of these reefs, and allow for projections of future state based on various scenarios.
			 </p>
			 <p style='font-size: 1.4em'>
			 DENR-BMB and Regional Offices were trained on coral taxonomy, methods on reef assessment and monitoring, and data handling and analyses, which they use on our NIPAS sites.
			 </p><br>
